/**
 * add -addtion
 * @a: integer
 * @b: 2nd integer
 * Return: something
 */
int add(int a, int b)
{

	return (a + b);
}

/**
 * sub - substraction
 * @a: integer
 * @b: 2nd interger
 * Return: Somethin
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplication
 * @: 1st integer
 * @b: 2nd integer
 * Return: something
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - division
 * @: 1st integer
 * @b: 2nd integer
 * Return: something
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulus for remainder
 * @: 1st integer
 * @b: 2nd integer
 * Return: something
 */
int mod(int a, int b)
{

	return (a % b);
}
