/* add - addition
 * @a: the integer
 * @b: the int or integer
 * Return: sucess
 */
int add(int a, int b)
{
return (a + b);
}
/**
 * sub - substraction
 * @a: the integer
 * @b: the int or integer
 * Return: Sucess
 */
int sub(int a, int b)
{
return (a - b);
}
/**
 * mul - multiplication
 * @a: integer
 * @b: the in or integer
 * Return: Success
 */
int mul(int a, int b)
{
return (a * b);
}
/**
 * div - division
 * @a: the interger
 * @b: int or integer
 * Return: success
 */
int div(int a, int b)
{
return (a / b);
}
/**
 * mod - modulus(remainder)
 * @a: the integer
 * @b: int or integer
 * Return: success
 */
int mod(int a, int b)
{
return (a % b);
}

